#include <unistd.h>

int    main(int ac, char **av)
{
    int    i;
    int    j;

    i = 0;
    j = 0;
    if (ac == 2)
    {
        while (av[1][i])
            i++;
        i--;
        while (av[1][i] && i >= 0)
        {
            while (av[1][i] != '\t' && av[1][i] != ' ' && i >= 0)
            {
                j = i;
                if (av[1][j - 1] == '\t' || av[1][j - 1] == ' ' || av[1][j - 1] == 0)
                {
                    while (av[1][j] != '\t' && av[1][j] != ' ' && av[1][j] != '\0')
                        write(1, &av[1][j++], 1);
                    if (i > 1)
                        write(1, " ", 1);
                }
                i--;
            }
            i--;
        }
    }
    write (1, "\n", 1);
}